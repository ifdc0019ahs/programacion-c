// Tipos de datos numericos
#include <stdio.h>
int main() {
    // Sumar dos numeros enteros
    int num1 = 10; // Declaramos una variable de tipo entero y le asignamos el valor 10
    int num2 = 5; // Declaramos otra variable de tipo entero y le asignamos el valor 5
    int resultado = num1 + num2; // Almacenamos el resultado de la suma en otra variable de tipo entero
    printf("El resultado de la suma es: %d\n", resultado); // Imprime el resultado de la suma en la consola
    return 0; // Devuelve 0 para indicar que el programa terminó correctamente
}